function formatDuration(durationSeconds) {
  if (durationSeconds) {
    const minutes = Math.floor(durationSeconds / 60);
    const seconds = durationSeconds % 60;
    const formattedDuration = `${minutes}:${seconds
      .toFixed(0)
      .padStart(2, "0")}`;
    return formattedDuration;
  } else return "";
}

